#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*temp;

	temp = ft_lstlast(*lst);
	if (temp == 0)
		*lst = new;
	else
		temp->next = new;
	return ;
}
